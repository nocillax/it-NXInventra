import { apiFetch } from "@/lib/apiClient";
import { Item } from "@/types/shared";

export async function getItems(inventoryId: string): Promise<Item[]> {
  try {
    const items: Item[] = await apiFetch(`/items?inventoryId=${inventoryId}`);
    return items;
  } catch (error) {
    console.error(`Failed to fetch items for inventory ${inventoryId}:`, error);
    return [];
  }
}
